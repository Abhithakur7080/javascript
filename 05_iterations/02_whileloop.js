//while loop
/*
while (condition) {
    ...statement
}
*/
let index = 0;
while (index <= 10) {
  console.log(`Value of index is ${index}`);
  index = index + 2;
}
let myArray = ["flash", "batman", "superman"];

let arr = 0;
while (arr < myArray.length) {
  console.log(`Value of myArray is ${myArray[arr]}`);
  arr++;
}
//do while loop
/*
do{
    ...statement
} while (condition) {
    ...statement
}
*/
let score = 1;
do {
  console.log(`Score is ${score}`);
  score++;
} while (score <= 10);
